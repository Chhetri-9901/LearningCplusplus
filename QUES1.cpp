// Program to print values of variables of different data types

#include <iostream>
using namespace std;

int main() {

    int num {50};
    char c = 'a';
    float n = 5.453;
    double d = 56.56432;
    char msg [] = "Hi";

    cout << num << " " << c << " " << n << " " << d << " " << msg << endl;

    return 0;
}